#Input Two numbers
num1 = float(input("Enter Number 1: "))
num2 = float(input("Enter Number 2: "))

#Find the smaller number
if num1 > num2:
    #Print The smaller number
    print(num2)
else:
    #Print The smaller number
    print(num1)
    